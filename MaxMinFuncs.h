#ifndef MaxMinFuncs_h
#define MaxMinFuncs_h

static inline int maxInt(int a, int b)
{
    return a > b ? a : b;
}

static inline short minShort(short a, short b)
{
    return a < b ? a : b;
}

#endif /* MaxMinFuncs_h */
